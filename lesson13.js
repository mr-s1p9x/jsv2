// Операторы присваивания и сравнения
let age = 18 + 5;
age += 2; // увеличиваем на 2. age = age + 2.
age -= 3; // age = age - 3;
age *= 2; // age = age * 2;
age /= 2; // age = age / 2;

age++; // age = age + 1;
age--; // age = age - 1;

console.log(age);

// Операторы сравнения
const vasia = 20;
console.log(age > vasia); // true 
console.log(age >= vasia); // true
console.log(age < vasia); // false
console.log(age <= vasia); // false
console.log(age == vasia); // false
